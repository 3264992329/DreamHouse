package HashSet;

import java.util.HashSet;

public class myHashSet01 {
    public static void main(String[] args) {
        HashSet<s> sb=new HashSet<>();
        s sb1=new s("sss");
        s sb2=new s("bbb");
        s sb3=new s("ccc");
        s sb4=new s("eee");
        s sb5=new s("bbb");
        System.out.println(sb.add(sb1));
        System.out.println(sb.add(sb2));
        System.out.println(sb.add(sb3));
        System.out.println(sb.add(sb4));
        System.out.println(sb.add(sb5));
        System.out.println(sb);
    }
}
