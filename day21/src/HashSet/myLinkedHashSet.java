package HashSet;

import java.util.LinkedHashSet;

public class myLinkedHashSet {
    public static void main(String[] args) {
        LinkedHashSet<s> st=new LinkedHashSet<>();
        s s1=new s("aaa");
        s s2=new s("bbb");
        s s3=new s("ccc");
        s s4=new s("ddd");
        st.add(s1);
        st.add(s2);
        st.add(s3);
        st.add(s4);
        System.out.println(st);

    }
}
